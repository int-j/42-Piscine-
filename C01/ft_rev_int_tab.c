#include<unistd.h>
void	ft_rev_int_tab(int *tab, int size)

{
	int	i;
	int	s;

	i = 0;
	while (i < (size / 2))
	{
		s = tab[i];
		tab[i] = tab [size - i - 1];
		tab[size - i - 1] = s;
		i++;
	}
}
