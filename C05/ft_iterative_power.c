int	ft_iterative_power(int nb, int power)
{
	int	i;
	int	po;

	i = 0;
	po = 0;
	if (nb == 0 && power == 0)
		return (1);
	while (i < power - 1)
	{
		po += (nb * nb);
		i++;
	}
	return (po);
}
