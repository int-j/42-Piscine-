char	*ft_strdup(char *src)
{
	char *s2;
	int i = 0;
	int size = 0;
	s2 = (char*)malloc(sizeof(char) * size + 1);
	while( src[i] != '\0')
		{
			s2[i] = src[i]; 
			i++;
		}
		s2[i] = '\0';
		return(s2);
}
